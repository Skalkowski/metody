package metody3;

public class Euler {
	private double x;
	private double y;

	public Euler(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}

	public Euler() {
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

}

package metody3;

public class Porownywalna {
	private double x;
	private double y;
	
	public Porownywalna(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public Porownywalna() {
		super();
	}
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
